    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Persona persona=new Persona(txtnombres.getText(), txtapellidos.getText(), 
                                    txttelefono.getText(), txtfecha.getText(), 
                                    txtcedula.getText(), txtcorreo.getText(), txtcontraseña.getText());
        lista.add(persona);
        
        mostrar();
        
    }//GEN-LAST:event_jButton1ActionPerformed
    public void mostrar() {
        String matriz[][]=new String[lista.size()][7];
        for (int i = 0; i <lista.size(); i++) {
            matriz[i][0]=lista.get(i).getNombres();
            matriz[i][1]=lista.get(i).getApellidos();
            matriz[i][2]=lista.get(i).getTelefono();
            matriz[i][3]=lista.get(i).getFecNaci();
            matriz[i][4]=lista.get(i).getCedula();
            matriz[i][5]=lista.get(i).getCorreo();
            matriz[i][6]=lista.get(i).getContraseña();
        }
        jTablePersona.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "NOMBRES", "APELLIDOS", "TELEFONO", "FECHA NACIMIENTO", "CEDULA", "CORREO", "CONTRASEÑA"
            }
        ));
    }
